    public int createTitle(title obj) throws SQLException
    {
        dbConnection conn = dbConnection.getConnection();
 
        java.sql.PreparedStatement ps= conn.con.prepareStatement("insert into title (titleName, typeID, availability, mediaFormat, releaseYear, genre, description) values (?,?,?,?,?,?,?)");
        ps.setString(1, obj.getTitleName());
        ps.setInt(2, obj.getType().getTypeID());
        ps.setBoolean(3, obj.getAvailability());
        ps.setString(4, obj.getMediaFormat());
        ps.setInt(5,obj.getReleaseYear());
        ps.setString(6, obj.getGenre());
        ps.setString(7, obj.getDescription());
                        
        return ps.executeUpdate();   
        
    }


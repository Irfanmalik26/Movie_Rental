    private void addTitleBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addTitleBtnActionPerformed
        title t1 = new title();
        type t;
        if(typeChoice.getSelectedIndex()== 0){
            t = new music();
            t1.setMediaFormat("CD");
        }
        else if(typeChoice.getSelectedIndex()== 1){
            t = new liveConcertVideos(); 
             t1.setMediaFormat("Blu-Ray");
        }
        else if(typeChoice.getSelectedIndex()== 2){
            t = new TV();
             t1.setMediaFormat("DVD");
        }
        else{
            t = new VL();
             t1.setMediaFormat("DVD");
        }

        t1.setTitleName(this.titleName.getText());
        t1.setType(t);
        t1.setReleaseYear(Integer.parseInt(this.releaseYear.getText()));
        t1.setGenre(this.genre.getText());
        t1.setDescription(this.description.getText());
        
        if(this.availability.getSelectedItem()=="True")
            t1.setAvailability(true);
        else
            t1.setAvailability(false);
               
        int flag=0;
        try {
            flag = createTitle(t1);
        } catch (SQLException ex) {
            Logger.getLogger(addCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if(flag==0) {
            JOptionPane.showMessageDialog(this, "Title NOT Added!");
        } else {
            JOptionPane.showMessageDialog(this, "Title Added!");
        }
        this.setVisible(false);
        new homepage().setVisible(true);
    }//GEN-LAST:event_addTitleBtnActionPerformed

